class Nivel():
    def __init__(self, rolagem):
        self.rolagem = rolagem

        plat_comum = pygame.image.load('assets/tiles/plataforma_comum.png')

        self.posicoes_validas = [] # lista que irá conter as coordenadas de todos os 1's


        # adicionando todas as coordenadas validas a lista posicoes_validas
        linha_coord = 0
        for linha in layout_nivel:
            valor_coord = 0
            for valor in linha:
                if valor == 1:
                    plat_comum = pygame.transform.scale(plat_comum, TAMANHO_TILE)
                    plat_comum_rect = plat_comum.get_rect()
                    plat_comum_rect.x = valor_coord * TAMANHO_TILE[0]
                    plat_comum_rect.y = linha_coord * TAMANHO_TILE[1]
                    self.posicoes_validas.append((plat_comum, plat_comum_rect))
                valor_coord += 1

            linha_coord += 1

    # camera
    def cam_rolagem(self, plataforma = pygame.math.Vector2(0, 0)):
        rolagem = self.rolagem
        self.plataforma_pos = (plataforma.x - rolagem[0], plataforma.y - rolagem[1])
        return self.plataforma_pos

    # funcao responsavel por desenhar na tela as plataformas com base na matriz de coordenadas
    def plataformas_nivel(self):
        tela = pygame.display.get_surface() # permite se referir a tela sem pegar nenhum parâmetro
        for posicao in self.posicoes_validas:
            self.plataforma_pos = self.cam_rolagem(posicao[1])
            tela.blit(posicao[0], self.plataforma_pos)


class Coletavel():
    def __init__(self, nivel):
        self.nivel = nivel

        self.coletavel_azul = pygame.image.load('assets/coletaveis/azul.png')
        self.coletavel_verde = pygame.image.load('assets/coletaveis/verde.png')
        self.coletavel_rosa = pygame.image.load('assets/coletaveis/rosa.png')

        # separa os coletaveis em listas
        self.coletaveis_azuis = []
        self.coletaveis_verdes = []
        self.coletaveis_rosas = []

        linha_coord = 0
        for linha in layout_nivel:
            valor_coord = 0
            for valor in linha:
                valor_coord += 1

                if valor == 2:
                    self.coletavel_azul = pygame.transform.scale(self.coletavel_azul, TAMANHO_TILE)
                    coletavel_azul_rect = self.coletavel_azul.get_rect()
                    coletavel_azul_rect.x = valor_coord * TAMANHO_TILE[0]
                    coletavel_azul_rect.y = linha_coord * TAMANHO_TILE[1]
                    self.coletaveis_azuis.append((self.coletavel_azul, coletavel_azul_rect))

                if valor == 3:
                    self.coletavel_verde = pygame.transform.scale(self.coletavel_verde, TAMANHO_TILE)
                    coletavel_verde_rect = self.coletavel_verde.get_rect()
                    coletavel_verde_rect.x = valor_coord * TAMANHO_TILE[0]
                    coletavel_verde_rect.y = linha_coord * TAMANHO_TILE[1]
                    self.coletaveis_verdes.append((self.coletavel_verde, coletavel_verde_rect))

                if valor == 4:
                    self.coletavel_rosa = pygame.transform.scale(self.coletavel_rosa, TAMANHO_TILE)
                    coletavel_rosa_rect = self.coletavel_rosa.get_rect()
                    coletavel_rosa_rect.x = valor_coord * TAMANHO_TILE[0]
                    coletavel_rosa_rect.y = linha_coord * TAMANHO_TILE[1]
                    self.coletaveis_rosas.append((self.coletavel_rosa, coletavel_rosa_rect))
            linha_coord += 1

    def objetos_nivel(self):
        tela = pygame.display.get_surface() # permite se referir a tela sem pegar nenhum parâmetro
        for posicao in self.coletaveis_azuis:
            posicao_coletavel = self.nivel.cam_rolagem(posicao[1])
            tela.blit(posicao[0], posicao_coletavel)
        for posicao in self.coletaveis_verdes:
            posicao_coletavel = self.nivel.cam_rolagem(posicao[1])
            tela.blit(posicao[0], posicao_coletavel)
        for posicao in self.coletaveis_rosas:
            posicao_coletavel = self.nivel.cam_rolagem(posicao[1])
            tela.blit(posicao[0], posicao_coletavel)
